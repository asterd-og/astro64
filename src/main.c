uint8_t *load_file(char *file_name, size_t *size) {
    FILE *file = fopen(file_name, "rb");
    if (!file) return NULL;
    fseek(file, 0, SEEK_END);
    *size = ftell(file);
    rewind(file);
    uint8_t *buffer = (uint8_t*)malloc(*size);
    fread(buffer, 1, *size, file);
    return buffer;
}

void *monitor_thread(void *arg) {
    vm_t *vm = (vm_t*)arg;
    char buffer[100];
    memset(buffer, 0, 100);
    while (vm_running) {
        printf("monitor> ");
        scanf("%100s", &buffer);
        if (!strcmp(buffer, "dump")) {
            vm_dump(vm);
            memset(buffer, 0, 100);
        } else if (!strcmp(buffer, "exit")) {
            vm_running = false;
            break;
        }
    }
    return NULL;
}

static inline bool bit_address_from_mask(uint32_t convert, int ret) {
    int mask = 1 << (ret - 1);
    return ((convert & mask) != 0);
}

double last_cursor_time = 0.0f;
bool draw_cursor = true;

uint32_t color_palette[] = {
    0x000000FF,
    0x555555FF,
    0xAAAAAAFF,
    0xFFFFFFFF,
    0xFF0000FF,
    0x00FF00FF,
    0x0000FFFF,
    0xFFFF00FF,
    0xFF00FFFF,
    0x00FFFFFF,
    0xFFA500FF,
    0x800080FF,
    0x8B4513FF,
    0xFFC0CBFF,
    0x90EE90FF,
    0xADD8E6FF
};

uint32_t get_color_from_palette(vm_t *vm, uint8_t color) {
    return *(uint32_t*)(vm->ram + 0x10010 + (color * 4));
}

void draw_screen(vm_t *vm) {
    ClearBackground(BLACK);
    Color last_fg = GetColor(color_palette[2]);
    for (uint32_t idx = 0; idx < rows * cols * 2; idx += 2) {
        uint32_t point = vm->ram[0x10100+idx] * font_height;
        if (!point) continue;
        uint8_t color = vm->ram[0x10100+(idx+1)];
        uint8_t fg = color & 0xF;
        uint8_t bg = (color >> 4);
        last_fg = GetColor(get_color_from_palette(vm, fg));
        uint32_t x = (idx / 2) % cols;
        uint32_t y = (idx / 2) / cols;
        DrawRectangle(x * font_width, y * font_height, font_width, font_height, GetColor(get_color_from_palette(vm, bg)));
        for (uint32_t i = 0; i < font_height; i++)
            for (uint32_t j = 0; j < font_width; j++)
                if (bit_address_from_mask(font[point+i], j + 1)) {
                    uint32_t cx = (x * font_width) + (font_width - j);
                    uint32_t cy = (y * font_height) + i;
                    DrawPixel(cx, cy, last_fg);
                }
    }
    if (GetTime() - last_cursor_time > 0.5f) {
        last_cursor_time = GetTime();
        draw_cursor = !draw_cursor;
    }
    if (draw_cursor) {
        uint32_t cursor_x = *(uint32_t*)(vm->ram + 0x10000) * font_width;
        uint32_t cursor_y = *(uint32_t*)(vm->ram + 0x10004) * font_height;
        DrawRectangle(cursor_x, cursor_y, font_width, font_height, last_fg);
    }
}

void *vm_thread(void *arg) {
    vm_t *vm = (vm_t*)arg;
    while (vm_running) {
        vm_clock(vm);
    }
    return NULL;
}

uint64_t convert_to_bytes(const char *str) {
    uint64_t value = 0;
    char unit = 0;
    int i = 0;

    while (str[i] && isdigit(str[i])) {
        value = value * 10 + (str[i] - '0');
        i++;
    }

    if (str[i])
        unit = toupper(str[i]);

    switch (unit) {
        case 'K':
            value *= 1024;
            break;
        case 'M':
            value *= 1024 * 1024;
            break;
        case 'G':
            value *= 1024 * 1024 * 1024;
            break;
        default:
            break;
    }

    return value;
}

int main(int argc, char **argv) {
    cols = screen_width / font_width;
    rows = screen_height / font_height;

    size_t ram_size = 10 * 1024 * 1024; // 10 MB

    char *firmware_name = "";
    char *disks_to_load[32];
    int disks_to_load_count = 0;

    for (int i = 1; i < argc; i++) {
        if (!strcmp(argv[i], "-m")) {
            i++;
            ram_size = convert_to_bytes(argv[i]);
        } else if (!strcmp(argv[i], "--firmware")) {
            i++;
            firmware_name = argv[i];
        } else if (!strcmp(argv[i], "--disk")) {
            i++;
            char *disk_name = argv[i];
            if (disks_to_load_count == 32) {
                printf("Error: Tried to load too many disks. (Max is 32.)\n");
                return 1;
            }
            disks_to_load[disks_to_load_count++] = disk_name;
        }
    }

    size_t rom_size = 0; // Max of 64K (afterwards comes devices info at 0x10000)
    uint8_t *rom = load_file(firmware_name, &rom_size);
    if (!rom) {
        printf("Error: Couldn't load firmware.\n");
        return 1;
    }
    if (rom_size > 64 * 1024) {
        printf("Error: Firmware size exceeds 64K.\n");
        return 1;
    }
    vm_t *vm = vm_create(ram_size);
    for (int i = 0; i < disks_to_load_count; i++) {
        char disk_name[16];
        sprintf(disk_name, "ASTRO64 DISK #%02d", i);
        if (vm_disk_load(disks_to_load[i], disk_name)) {
            printf("Error: Failed to load disk %d.\n", i);
            vm_destroy(vm);
            return 1;
        }
    }
    vm_load_rom(vm, rom, rom_size);

    InitWindow(screen_width, screen_height, "Astro64 Emulator");

    vm_init_devices(vm);

    vm_write32(vm, 0x10008, cols);
    vm_write32(vm, 0x1000C, rows);

    for (int i = 0; i < 16; i++) {
        vm_write32(vm, 0x10010 + (i * 4), color_palette[i]);
    }

    // Monitor
    pthread_t monitor;
    pthread_create(&monitor, NULL, monitor_thread, vm);

    pthread_t vm_th;
    pthread_create(&vm_th, NULL, vm_thread, vm);

    while (vm_running) {
        if (WindowShouldClose()) {
            vm_running = false;
            break;
        }

        BeginDrawing();
        draw_screen(vm);
        EndDrawing();
    }

    CloseWindow();
    vm_dump(vm);
    pthread_cancel(monitor);
    vm_unload_devices();
cleanup:
    vm_destroy(vm);
    return 0;
}
